package com.srinivas.practice;

public class TwoPower {
    public static void main(String[] args) {
        System.out.println("2 ^ 63 = " + (1<<62));
        System.out.println("2 ^ 4 = " + (1<<4));
        System.out.println("2 ^ 3 = " + (1<<3));
        System.out.println("2 ^ 2 = " + (1<<2));

    }
}
